def main():
    """Основная функция генерации страниц"""
    
    base_dir = "/workspace/site_content"
    os.makedirs(base_dir, exist_ok=True)
    
    total_pages = 0
    
    print("Начинаем генерацию страниц...")
    
    # Генерируем страницы для каждого города
    for city in CITIES:
        print(f"\nГенерация страниц для города: {city}")
        
        # Создаем директорию для города
        city_dir = os.path.join(base_dir, city)
        os.makedirs(city_dir, exist_ok=True)
        
        # Генерируем главную страницу города (ссылочная)
        city_page = generate_city_landing_page(city)
        city_page_path = os.path.join(city_dir, "index.html")
        
        with open(city_page_path, 'w', encoding='utf-8') as f:
            f.write(city_page)
        
        print(f"  ✓ Создана главная страница города: {city}/index.html")
        total_pages += 1
        
        # Генерируем страницы для каждой услуги
        for service in SERVICES:
            service_slug = service.lower().replace(" ", "-")
            service_dir = os.path.join(city_dir, service_slug)
            os.makedirs(service_dir, exist_ok=True)
            
            # Генерируем контент страницы услуги
            service_page = generate_service_page(city, service)
            service_page_path = os.path.join(service_dir, "index.html")
            
            with open(service_page_path, 'w', encoding='utf-8') as f:
                f.write(service_page)
            
            print(f"  ✓ Создана страница услуги: {city}/{service_slug}/index.html")
            total_pages += 1
    
    print(f"\n{'='*60}")
    print(f"Генерация завершена!")
    print(f"Всего создано страниц: {total_pages}")
    print(f"Городов: {len(CITIES)}")
    print(f"Услуг на город: {len(SERVICES)}")
    print(f"Страниц городов: {len(CITIES)}")
    print(f"Страниц услуг: {len(CITIES) * len(SERVICES)}")
    print(f"{'='*60}")
    print(f"\nСтруктура директорий:")
    print(f"{base_dir}/")
    for city in CITIES:
        print(f"  └── {city}/")
        print(f"      ├── index.html (главная страница города)")
        for service in SERVICES[:3]:
            service_slug = service.lower().replace(" ", "-")
            print(f"      └── {service_slug}/index.html")
        print(f"      └── ... (еще {len(SERVICES) - 3} услуг)")
    
    # Сохраняем конфигурацию
    config = {
        "cities": CITIES,
        "services": SERVICES,
        "total_pages": total_pages,
        "generated_at": datetime.now().isoformat()
    }
    
    config_path = os.path.join(base_dir, "config.json")
    with open(config_path, 'w', encoding='utf-8') as f:
        json.dump(config, f, ensure_ascii=False, indent=2)
    
    print(f"\nКонфигурация сохранена в: {config_path}")


if __name__ == "__main__":
    main()
