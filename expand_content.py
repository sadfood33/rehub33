def expand_html_file(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    city = get_city_from_path(filepath)
    service = get_service_from_path(filepath)
    region = get_region_from_path(filepath)
    
    insert_pattern = r'(<section>\s*<h2>Другие услуги в городе)'
    match = re.search(insert_pattern, content)
    
    if match:
        insert_pos = match.start()
        additional_content = generate_extended_content(city, service, region)
        new_content = content[:insert_pos] + additional_content + "\n\n" + content[insert_pos:]
        
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(new_content)
        return True
    else:
        footer_pattern = r'(</div>\s*<footer>)'
        match = re.search(footer_pattern, content)
        if match:
            insert_pos = match.start()
            additional_content = generate_extended_content(city, service, region)
            new_content = content[:insert_pos] + "<div class=\"container content\">" + additional_content + "</div>\n\n" + content[insert_pos:]
            
            with open(filepath, 'w', encoding='utf-8') as f:
                f.write(new_content)
            return True
    
    return False

if __name__ == '__main__':
    import sys
    if len(sys.argv) < 2:
        print("Usage: python expand_content.py <file1> [file2] ...")
        sys.exit(1)
    
    for filepath in sys.argv[1:]:
        if os.path.exists(filepath):
            print(f"Processing: {filepath}")
            try:
                expand_html_file(filepath)
                print(f"  OK")
            except Exception as e:
                print(f"  Error: {e}")
        else:
            print(f"  Not found: {filepath}")
