def get_city_from_path(filepath):
    parts = filepath.split('/')
    for i, part in enumerate(parts):
        if part == 'moskva' or part == 'yaroslavl':
            if i + 1 < len(parts):
                next_part = parts[i + 1]
                if next_part.endswith('.html'):
                    return next_part.replace('.html', '').replace('_', ' ').title()
                elif i + 2 < len(parts):
                    return parts[i + 2].replace('.html', '').replace('_', ' ').title()
    return "Город"

def get_service_from_path(filepath):
    filename = os.path.basename(filepath)
    service = filename.replace('.html', '').replace('-', ' ').replace('_', ' ')
    return service.title()

def get_region_from_path(filepath):
    if '/yaroslavl/' in filepath:
        return "Ярославская область"
    return "Московская область"

